package com.Ako.finaltest;

public class FinalTest {
    public static void main(String[] args){
        /*
        final 修饰变量，此时叫做常量
               特点1：只能被赋值一次，一旦复制，无法再次修改
               特点2：常量名大写，多个单词之间用下划线隔开
        */
        //定义一个常量,一般全用大写噢！
        final int NUMBER=100;
        System.out.println(NUMBER);

    }
}
